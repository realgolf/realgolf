<h1>Download</h1>

<p>Here we will explain how to add the site as a Progressive Web App (PWA) on your device.</p>

<h2>Safari (iOS)</h2>
<p>To install this site as a PWA on Safari for iOS:</p>
<ol>
	<li>Tap the Share icon (the square with an arrow pointing up) at the bottom of the screen.</li>
	<li>Scroll down and tap "Add to Home Screen".</li>
	<li>Confirm by tapping "Add".</li>
</ol>

<h2>Chrome (Android)</h2>
<p>To install this site as a PWA on Chrome for Android:</p>
<ol>
	<li>Tap the three-dot menu icon at the top right corner of the screen.</li>
	<li>Select "Add to Home screen" from the menu.</li>
	<li>Review the details and tap "Add".</li>
</ol>

